function myCal($number1, $number2, $operators){
    if( $operators == "+" ){
        return $number1 + $number2;
    }elseif( $operators == "-" ){
        return $number1 - $number2;
    }elseif( $operators == "*" ){
        return $number1 * $number2;
    }elseif( $operators == "/" ){
        return $number1 / $number2;
    }elseif( $operators == "%" ){
        return $number1 % $number2;
    }elseif( $operators == "pi"){
        return pi();
    }elseif( $operators == "abs" ){
        return abs( $number1 );
    }elseif( $operators == "sqrt" ){
        return sqrt( $number1 );
    }elseif( $operators == "round" ){
        return round( $number2 );
    }elseif( $operators == "rand" ){
        return rand( $number1, $number2 );
    }
}
