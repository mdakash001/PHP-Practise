function practiseMath($a,$b,$type){
    switch ($type){
        case "sum":
            echo $a+$b;
            break;
            case "sub":
                echo $a-$b;
                break;
                case "mul":
                    echo $a*$b;
                    break;
                    case "divi":
                        echo $a/$b;
                        break;
                        case "pi":
                            echo pi();
                            break;
                            case "rand":
                                echo rand();
                                break;
                        default: echo "This math is End";
    }
}
